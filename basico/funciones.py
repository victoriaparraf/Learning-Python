#funciones

def Suma(a,b):
    print(a+b)

def Resta(a,b):
    print(a-b)

def Multiplicacion(a,b):
    print(a*b)

def Division(a,b):
    return a/b

def Potencia(a,b):
    return a**b

def Raiz(a):
    return a**0.5

Suma(2,3)
Resta(2,3)
Multiplicacion(2,3)
primero = Division(2,3)
segundo = Potencia(2,3)
tercero = Raiz(2)

print(primero)
print(segundo)
print(tercero)

